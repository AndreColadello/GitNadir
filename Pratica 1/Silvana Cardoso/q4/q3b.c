#include "q3b.h"

double soma (double n1, double n2, double n3)
{
    return n1+n2+n3;
}
double prod (double n1, double n2, double n3)
{
    return n1*n2*n3;
}
double div (double n1, double n2)
{
    return n1/n2;
}
double m (double n1, double n2, double n3)
{
    return ((5*n1)+(3*n2)+(2*n3))/10;
}
